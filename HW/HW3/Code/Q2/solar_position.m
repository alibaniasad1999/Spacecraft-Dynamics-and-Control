function [lamda, obliquity, r_sun] = solar_position(jd)
AU = 149597870.691;
n = jd - 2451545;
M = 357.528 + 0.9856003 * n;
M = mod(M, 360);
L = 280.460 + 0.98564736 * n;
L = mod(L, 360);
lamda = L + 1.915*sind(M) + 0.020 * sind(2*M);
lamda = mod(lamda, 360);
obliquity = 23.439 - 0.0000004 * n;
u = [cosd(lamda); sind(lamda) * cosd(obliquity); sind(lamda) * sind(obliquity)];
rS = (1.00014 - 0.01671 * cosd(M) - 0.000140 * cosd(2 * M)) * AU;
r_sun = rS*u;